import logging

from aiogram import F, Router
from aiogram.types import CallbackQuery

from infrastructure.database.models import Employee
from infrastructure.database.repo.STP.requests import MainRequestsRepo
from tgbot.filters.role import DutyFilter
from tgbot.keyboards.user.game.main import GameMenu
from tgbot.keyboards.user.game.products import (
    DutyPurchaseActionMenu,
    DutyPurchaseActivationMenu,
    duty_products_activation_kb,
    duty_purchases_detail_kb,
)

duty_game_products_router = Router()
duty_game_products_router.message.filter(F.chat.type == "private", DutyFilter())
duty_game_products_router.callback_query.filter(
    F.message.chat.type == "private", DutyFilter()
)

logger = logging.getLogger(__name__)


@duty_game_products_router.callback_query(
    GameMenu.filter(F.menu == "products_activation")
)
async def duty_products_activation(
    callback: CallbackQuery,
    callback_data: GameMenu,
    user: Employee,
    stp_repo: MainRequestsRepo,
):
    """
    –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –º–µ–Ω—é –ø–æ–∫—É–ø–æ–∫ –¥–ª—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –¥–µ–∂—É—Ä–Ω—ã–º–∏
    –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –ø–æ–∫—É–ø–∫–∏ —Å–æ —Å—Ç–∞—Ç—É—Å–æ–º "review" –∏ manager_role == 3 (–î–µ–∂—É—Ä–Ω—ã–π)
    –î–µ–∂—É—Ä–Ω—ã–π –≤–∏–¥–∏—Ç —Ç–æ–ª—å–∫–æ –ø–æ–∫—É–ø–∫–∏ –∏–∑ —Å–≤–æ–µ–≥–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è (–ù–¢–ü –∏–ª–∏ –ù–¶–ö)
    """

    # –î–æ—Å—Ç–∞—ë–º –Ω–æ–º–µ—Ä —Å—Ç—Ä–∞–Ω–∏—Ü—ã –∏–∑ callback data, —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ = 1
    page = getattr(callback_data, "page", 1)

    # –ü–æ–ª—É—á–∞–µ–º –ø–æ–∫—É–ø–∫–∏ –æ–∂–∏–¥–∞—é—â–∏–µ –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ —Å manager_role == 3 (–î–µ–∂—É—Ä–Ω—ã–π)
    review_purchases = await stp_repo.purchase.get_review_purchases_for_activation(
        manager_role=3
    )

    if not review_purchases:
        await callback.message.edit_text(
            """<b>‚úçÔ∏è –ê–∫—Ç–∏–≤–∞—Ü–∏—è –ø—Ä–µ–¥–º–µ—Ç–æ–≤</b>

–ù–µ—Ç –ø—Ä–µ–¥–º–µ—Ç–æ–≤, –æ–∂–∏–¥–∞—é—â–∏—Ö –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ üòä""",
            reply_markup=duty_products_activation_kb(page, 0, []),
        )
        return

    # –§–∏–ª—å—Ç—Ä—É–µ–º –ø–æ–∫—É–ø–∫–∏ –ø–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—é –¥–µ–∂—É—Ä–Ω–æ–≥–æ
    # –î–µ–∂—É—Ä–Ω—ã–π –º–æ–∂–µ—Ç –≤–∏–¥–µ—Ç—å —Ç–æ–ª—å–∫–æ –ø–æ–∫—É–ø–∫–∏ –∏–∑ —Å–≤–æ–µ–≥–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    division_filtered_purchases = []
    for purchase_details in review_purchases:
        product = purchase_details.product_info
        if product.division == user.division:
            division_filtered_purchases.append(purchase_details)

    if not division_filtered_purchases:
        await callback.message.edit_text(
            """<b>‚úçÔ∏è –ê–∫—Ç–∏–≤–∞—Ü–∏—è –ø—Ä–µ–¥–º–µ—Ç–æ–≤</b>

–ù–µ—Ç –ø—Ä–µ–¥–º–µ—Ç–æ–≤, –æ–∂–∏–¥–∞—é—â–∏—Ö –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ üòä""",
            reply_markup=duty_products_activation_kb(page, 0, []),
        )
        return

    # –õ–æ–≥–∏–∫–∞ –ø–∞–≥–∏–Ω–∞—Ü–∏–∏
    purchases_per_page = 5
    total_purchases = len(division_filtered_purchases)
    total_pages = (total_purchases + purchases_per_page - 1) // purchases_per_page

    # –°—á–∏—Ç–∞–µ–º –Ω–∞—á–∞–ª–æ –∏ –∫–æ–Ω–µ—Ü —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    start_idx = (page - 1) * purchases_per_page
    end_idx = start_idx + purchases_per_page
    page_purchases = division_filtered_purchases[start_idx:end_idx]

    # –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –ø–æ–∫—É–ø–æ–∫ –¥–ª—è —Ç–µ–∫—É—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    purchases_list = []
    for counter, purchase_details in enumerate(page_purchases, start=start_idx + 1):
        purchase = purchase_details.user_purchase
        product = purchase_details.product_info

        # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ
        purchase_user = await stp_repo.employee.get_user(user_id=purchase.user_id)
        user_name = (
            purchase_user.fullname if purchase_user else f"ID: {purchase.user_id}"
        )

        if purchase_user and purchase_user.username:
            purchases_list.append(f"""{counter}. <b>{product.name}</b> - {purchase.bought_at.strftime("%d.%m.%Y –≤ %H:%M")}
<blockquote><b>üë§ –°–ø–µ—Ü–∏–∞–ª–∏—Å—Ç</b>
<a href='t.me/{purchase_user.username}'>{user_name}</a> –∏–∑ {product.division}

<b>üìù –û–ø–∏—Å–∞–Ω–∏–µ</b>
{product.description}</blockquote>""")
        else:
            purchases_list.append(f"""{counter}. <b>{product.name}</b> - {purchase.bought_at.strftime("%d.%m.%Y –≤ %H:%M")}
<blockquote><b>üë§ –°–ø–µ—Ü–∏–∞–ª–∏—Å—Ç</b>
<a href='tg://user?id={purchase_user.user_id if purchase_user else purchase.user_id}'>{user_name}</a> –∏–∑ {product.division}

<b>üìù –û–ø–∏—Å–∞–Ω–∏–µ</b>
{product.description}</blockquote>""")
        purchases_list.append("")

    message_text = f"""<b>‚úçÔ∏è –ê–∫—Ç–∏–≤–∞—Ü–∏—è –ø—Ä–µ–¥–º–µ—Ç–æ–≤</b>

{chr(10).join(purchases_list)}"""

    await callback.message.edit_text(
        message_text,
        reply_markup=duty_products_activation_kb(page, total_pages, page_purchases),
    )

    logger.info(
        f"[–î–µ–∂—É—Ä–Ω—ã–π] - [–ê–∫—Ç–∏–≤–∞—Ü–∏—è] {callback.from_user.username} ({callback.from_user.id}): –ü—Ä–æ—Å–º–æ—Ç—Ä –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –ø—Ä–µ–¥–º–µ—Ç–æ–≤, —Å—Ç—Ä–∞–Ω–∏—Ü–∞ {page}, –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ: {user.division}"
    )


@duty_game_products_router.callback_query(DutyPurchaseActivationMenu.filter())
async def duty_purchase_activation_detail(
    callback: CallbackQuery,
    callback_data: DutyPurchaseActivationMenu,
    stp_repo: MainRequestsRepo,
):
    """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –¥–µ—Ç–∞–ª—å–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ–∫—É–ø–∫–µ –∏ –ø—Ä–µ–¥–º–µ—Ç–µ –¥–ª—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏"""
    purchase_id = callback_data.purchase_id
    current_page = callback_data.page

    # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –ø–æ–∫—É–ø–∫–µ
    purchase_details = await stp_repo.purchase.get_purchase_details(purchase_id)

    if not purchase_details:
        await callback.message.edit_text(
            """<b>‚úÖ –ê–∫—Ç–∏–≤–∞—Ü–∏—è</b>

–ù–µ —Å–º–æ–≥ –Ω–∞–π—Ç–∏ –æ–ø–∏—Å–∞–Ω–∏–µ –¥–ª—è –ø—Ä–µ–¥–º–µ—Ç–∞ ‚òπ""",
            reply_markup=duty_purchases_detail_kb(purchase_id, current_page),
        )
        return

    purchase = purchase_details.user_purchase
    product = purchase_details.product_info

    # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ
    purchase_user: Employee = await stp_repo.employee.get_user(user_id=purchase.user_id)
    user_head: Employee = await stp_repo.employee.get_user(fullname=purchase_user.head)

    user_info = (
        f"<a href='t.me/{purchase_user.username}'>{purchase_user.fullname}</a>"
        if purchase_user and purchase_user.username
        else f"{purchase_user.fullname if purchase_user else f'ID: {purchase.user_id}'}"
    )
    head_info = (
        f"<a href='t.me/{user_head.username}'>{purchase_user.head}</a>"
        if user_head and user_head.username
        else purchase_user.head
        if purchase_user
        else "-"
    )

    message_text = f"""
<b>üéØ –ê–∫—Ç–∏–≤–∞—Ü–∏—è –ø—Ä–µ–¥–º–µ—Ç–∞</b>

<b>üèÜ –û –ø—Ä–µ–¥–º–µ—Ç–µ</b>  
<blockquote><b>–ù–∞–∑–≤–∞–Ω–∏–µ</b>
{product.name}

<b>üìù –û–ø–∏—Å–∞–Ω–∏–µ</b>
{product.description}

<b>üíµ –°—Ç–æ–∏–º–æ—Å—Ç—å</b>
{product.cost} –±–∞–ª–ª–æ–≤

<b>üî∞ –ù–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ</b>
{product.division}

<b>üìç –ê–∫—Ç–∏–≤–∞—Ü–∏–π</b>
{purchase.usage_count} ‚û°Ô∏è {purchase.usage_count + 1} ({product.count} –≤—Å–µ–≥–æ)</blockquote>"""

    message_text += f"""

<b>üë§ –û —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç–µ</b>
<blockquote><b>–§–ò–û</b>
{user_info}

<b>–î–æ–ª–∂–Ω–æ—Å—Ç—å</b>
{purchase_user.position if purchase_user else "-"} {purchase_user.division if purchase_user else "-"}

<b>–†—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å</b>
{head_info}</blockquote>

<b>üìÖ –î–∞—Ç–∞ –ø–æ–∫—É–ø–∫–∏</b>  
{purchase.bought_at.strftime("%d.%m.%Y –≤ %H:%M")}
"""
    await callback.message.edit_text(
        message_text,
        reply_markup=duty_purchases_detail_kb(purchase_id, current_page),
    )


@duty_game_products_router.callback_query(DutyPurchaseActionMenu.filter())
async def duty_purchase_action(
    callback: CallbackQuery,
    callback_data: DutyPurchaseActionMenu,
    stp_repo: MainRequestsRepo,
    user: Employee,
):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è/–æ—Ç–∫–ª–æ–Ω–µ–Ω–∏—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏ –¥–µ–∂—É—Ä–Ω—ã–º–∏"""
    purchase_id = callback_data.purchase_id
    action = callback_data.action

    try:
        # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ø–æ–∫—É–ø–∫–µ
        purchase_details = await stp_repo.purchase.get_purchase_details(purchase_id)

        if not purchase_details:
            await callback.answer("‚ùå –ü–æ–∫—É–ø–∫–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞", show_alert=True)
            return

        purchase = purchase_details.user_purchase
        product = purchase_details.product_info
        employee_user: Employee = await stp_repo.employee.get_user(
            user_id=purchase.user_id
        )

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –¥–µ–∂—É—Ä–Ω—ã–π –º–æ–∂–µ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –ø—Ä–µ–¥–º–µ—Ç—ã –∏–∑ —Å–≤–æ–µ–≥–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è
        if product.division != user.division:
            await callback.answer(
                f"‚ùå –¢—ã –º–æ–∂–µ—à—å –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –ø—Ä–µ–¥–º–µ—Ç—ã –∏–∑ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è {user.division}",
                show_alert=True,
            )
            return

        if action == "approve":
            # –ü–æ–¥—Ç–≤–µ—Ä–∂–¥–∞–µ–º –∞–∫—Ç–∏–≤–∞—Ü–∏—é –ø–æ–∫—É–ø–∫–∏
            await stp_repo.purchase.approve_purchase_usage(
                purchase_id=purchase_id,
                updated_by_user_id=callback.from_user.id,
            )

            await callback.answer(
                f"""‚úÖ –ü—Ä–µ–¥–º–µ—Ç '{product.name}' –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω!
                
–°–ø–µ—Ü–∏–∞–ª–∏—Å—Ç {employee_user.fullname} –±—ã–ª —É–≤–µ–¥–æ–º–ª–µ–Ω –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Å—Ç–∞—Ç—É—Å–∞""",
                show_alert=True,
            )

            if purchase.usage_count >= product.count:
                employee_notify_message = f"""<b>üëå –ü—Ä–µ–¥–º–µ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω:</b> {product.name}

–î–µ–∂—É—Ä–Ω—ã–π <a href='t.me/{user.username}'>{user.fullname}</a> –ø–æ–¥—Ç–≤–µ—Ä–¥–∏–ª –∞–∫—Ç–∏–≤–∞—Ü–∏—é –ø—Ä–µ–¥–º–µ—Ç–∞

–£ <b>{product.name}</b> –Ω–µ –æ—Å—Ç–∞–ª–æ—Å—å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–π 

<i>–ö—É–ø–∏—Ç—å –µ–≥–æ –ø–æ–≤—Ç–æ—Ä–Ω–æ –º–æ–∂–Ω–æ –≤ <b>üíé –ú–∞–≥–∞–∑–∏–Ω–µ</b></i>"""
            else:
                employee_notify_message = f"""<b>üëå –ü—Ä–µ–¥–º–µ—Ç –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω:</b> {product.name}

–î–µ–∂—É—Ä–Ω—ã–π <a href='t.me/{user.username}'>{user.fullname}</a> –ø–æ–¥—Ç–≤–µ—Ä–¥–∏–ª –∞–∫—Ç–∏–≤–∞—Ü–∏—é –ø—Ä–µ–¥–º–µ—Ç–∞

üìç –û—Å—Ç–∞–ª–æ—Å—å –∞–∫—Ç–∏–≤–∞—Ü–∏–π: {product.count - purchase.usage_count} –∏–∑ {product.count}"""

            await callback.bot.send_message(
                chat_id=employee_user.user_id,
                text=employee_notify_message,
            )

            logger.info(
                f"[–î–µ–∂—É—Ä–Ω—ã–π] - [–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ] {callback.from_user.username} ({callback.from_user.id}) –ø–æ–¥—Ç–≤–µ—Ä–¥–∏–ª {product.name} –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {employee_user.username} ({purchase.user_id})"
            )

        elif action == "reject":
            # –û—Ç–∫–ª–æ–Ω—è–µ–º –∞–∫—Ç–∏–≤–∞—Ü–∏—é –ø–æ–∫—É–ø–∫–∏
            await stp_repo.purchase.reject_purchase_usage(
                purchase_id=purchase_id, updated_by_user_id=callback.from_user.id
            )

            await callback.answer(
                f"""‚ùå –ê–∫—Ç–∏–≤–∞—Ü–∏—è –ø—Ä–µ–¥–º–µ—Ç–∞ '{product.name}' –æ—Ç–∫–ª–æ–Ω–µ–Ω–∞

–°–ø–µ—Ü–∏–∞–ª–∏—Å—Ç {employee_user.fullname} –±—ã–ª —É–≤–µ–¥–æ–º–ª–µ–Ω –æ–± –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Å—Ç–∞—Ç—É—Å–∞ –ø—Ä–µ–¥–º–µ—Ç–∞""",
                show_alert=True,
            )

            await callback.bot.send_message(
                chat_id=employee_user.user_id,
                text=f"""<b>–ê–∫—Ç–∏–≤–∞—Ü–∏—è –æ—Ç–º–µ–Ω–µ–Ω–∞:</b> {product.name}

–î–µ–∂—É—Ä–Ω—ã–π <a href='t.me/{user.username}'>{user.fullname}</a> –æ—Ç–º–µ–Ω–∏–ª –∞–∫—Ç–∏–≤–∞—Ü–∏—é –ø—Ä–µ–¥–º–µ—Ç–∞""",
            )

            logger.info(
                f"[–î–µ–∂—É—Ä–Ω—ã–π] - [–û—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ] {callback.from_user.username} ({callback.from_user.id}) –æ—Ç–∫–ª–æ–Ω–∏–ª –∞–∫—Ç–∏–≤–∞—Ü–∏—é {product.name} –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {employee_user.username} ({purchase.user_id})"
            )

        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º—Å—è –∫ —Å–ø–∏—Å–∫—É –ø–æ–∫—É–ø–æ–∫ –¥–ª—è –∞–∫—Ç–∏–≤–∞—Ü–∏–∏
        await duty_products_activation(
            callback=callback,
            callback_data=GameMenu(menu="products_activation"),
            user=user,
            stp_repo=stp_repo,
        )

    except Exception as e:
        logger.error(f"Error updating purchase status: {e}")
        await callback.answer("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –ø–æ–∫—É–ø–∫–∏", show_alert=True)
